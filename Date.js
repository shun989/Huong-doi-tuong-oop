class Date {
    constructor(day, month, year){
        this.day = day;
        this.month = month;
        this.year = year;
    }
    getDay(){
        return this.day;
    }
    gitDay(value){
        this.day = value;
    }
    getMonth(){
        return this.month;
    }
    gitMonth(value){
        this.month = value;
    }
    getYear(){
        return this.year;
    }
    gitYear(value){
        this.year = value;
    }
}
